while 1:

    for y in range(1, len(seats)-1): 
        adjCtr = 0
        rowAbove = seats[y-1]
        seatRow = seats[y]
        rowBelow = seats[y+1]
        newSeatRow = newSeats[y]
        
        for x in range(1, len(seatRow)-1):
            adjCtr = 0
            if seatRow[x] == "L" or seatRow[x] == "#":
                if rowAbove[x-1] == "#":
                    adjCtr += 1
                if rowAbove[x] == "#":
                    adjCtr += 1
                if rowAbove[x+1] == "#":
                    adjCtr += 1
                if seatRow[x-1] == "#":
                    adjCtr += 1
                if seatRow[x+1] == "#":
                    adjCtr += 1
                if rowBelow[x-1] == "#":
                    adjCtr += 1
                if rowBelow[x] == "#":
                    adjCtr += 1
                if rowBelow[x+1] == "#":
                    adjCtr += 1
                if adjCtr == 0:
                    newSeatRow[x] = "#"
                if adjCtr >= 4:
                    newSeatRow[x] = "L"
                adjCtr = 0

        newSeats[y] = newSeatRow
    if seats == newSeats:
        break

